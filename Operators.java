public class Operators {
    public static void main(String[] args){

        double x = 5;
        double y = 10;

        double z1 = x + y;
        double z2 = x - y;
        double z3 = x * y;
        double z4 = x / y;
        double z5 = x % y;

        
        System.out.println(z1);
        System.out.println(z2);
        System.out.println(z3);
        System.out.println(z4);
        System.out.println(z5);



    }
    
}
